import UIKit

class FriendlyColors {

  static func midGreen() -> UIColor {
    return UIColor(red: 51/255, green: 153/255, blue: 102/255, alpha: 1.0)
  }
  
  static func midBlue() -> UIColor {
    return UIColor(red: 54/255, green: 92/255, blue: 206/255, alpha: 1.0)
  }
  
  static func midGrey() -> UIColor {
    return UIColor(red: 140/255, green: 140/255, blue: 140/255, alpha: 1.0)
  }
  
  static func pink() -> UIColor {
    return UIColor(red: 250/255, green: 128/255, blue: 114/255, alpha: 1.0)
  }

}
